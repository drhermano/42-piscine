int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= 'a' && str[i] <= 'z')
				|| (str[i] >= 'A' && str[i] <= 'Z')))
			return (0);
		i++;
	}
	return (1);
}

#include <stdio.h>

int main(void)
{
	char *str = "Thisisaonetest";
	ft_str_is_alpha(str);
	printf("%s and %d\n", str, ft_str_is_alpha(str));
}