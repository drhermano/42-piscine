char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
#include <stdio.h>
int main(void)
{
	char dest[] = "JJJJJJJJ";
	char src[] = "Source";
	int n = 2;
	ft_strncpy(dest, src, n);
}
